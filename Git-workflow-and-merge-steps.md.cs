Командата:

git push origin --set-upstream add-title
се използва, когато създаваш нов клон локално и го изпращаш за първи път в отдалеченото хранилище.

Какво прави тази команда?
git push origin add-title: Изпраща локалния клон add-title към отдалеченото хранилище (GitHub или друг Git сървър) и го създава там.
--set-upstream: Свързва локалния клон add-title с отдалечения клон със същото име (или зададеното име), за да не се налага всеки път да указваш отдалечения клон при git pull или git push. След тази команда Git ще знае автоматично кой е "upstream" клонът.
    
    Кога и защо да използваш това?
Когато създаваш нов локален клон: 
Например:
git checkout -b add-title
    Тук създаваш нов клон с име add-title и добавяш промени в него.

Сега, когато искаш да изпратиш този клон в отдалеченото хранилище за първи път:
git push origin --set-upstream add-title
    Локалния клон add-title се изпраща в отдалеченото хранилище и се свързва с отдалечения клон add-title. 


След това можеш да използваш просто:
git push    # За изпращане на промени
git pull    # За изтегляне на промени

За удобство: 
Ако не зададеш --set-upstream, при всяко използване на git push или git pull ще трябва да указваш ръчно клона:
    
git push origin add-title
git pull origin add-title


Примерен сценарий:
Създаване на клон и правене на промени:

git checkout -b add-title
echo "New title feature" > title.txt
git add .
git commit -m "Add title feature"


Качване на клона за първи път:
git push origin --set-upstream add-title


След това можеш просто да използваш:

git push
git pull

Резултат:
След изпълнение на командата, Git свързва локалния клон add-title с отдалечения клон add-title.
Това улеснява работата ти в бъдеще.



    Сливане на клонове => Как да сливаме клонове в Git?
    Когато работиш в екип, често ще се наложи да сливаш промени от различни клонове. 
    Например, ако всеки член на екипа работи в отделен клон, ще трябва да сливате промените в основния клон (main).

За да слееш (merge) два клона, можеш да използваш командата git merge. 
Ето стъпките, които трябва да следваш:

Сценарий: Сливане на клон add-title в основния клон main
Увери се, че си в основния клон (main)

Ако не си, премини към него:
git checkout main
    
Изтегли последните промени от отдалеченото хранилище, за да се увериш, че работиш с актуалния main:
git pull origin main


Слей клона add-title в main (или друг клон) с командата git merge

Изпълни командата за сливане:

git merge add-title

    Това ще слee клон add-title в основния клон (main).
Разрешаване на конфликти (ако има такива)

Ако има конфликти (разлики в едни и същи части от файловете), Git ще те уведоми.

Отвори файловете с конфликти, където ще видиш нещо като това:
<<<<<<< HEAD
(Код от `main`)
=======
(Код от `add-title`)
>>>>>>> add-title

Избери коя версия на кода искаш да остане или комбинирай двете, след което запази промените.
След разрешаването на конфликтите, добави файловете отново:

git add .
git commit -m "Resolve merge conflicts"

Потвърди сливането
Ако няма конфликти, Git автоматично ще направи сливането и ще създаде merge commit. 
Резултатът ще изглежда така:

Merge made by the 'recursive' strategy.

Изпрати сливането към отдалеченото хранилище
След успешното сливане, качи промените в GitHub:
git push origin main
    След това можеш да изтриеш клон add-title, ако вече не ти е нужен
    git branch -d add-title
    Или можеш да го изтриеш директно от GitHub.


Примерен процес

1. Създаване на нов клон и добавяне на промени:
git checkout -b add-title
echo "New title feature" > title.txt
git add .
git commit -m "Add title feature"
git push origin --set-upstream add-title

2. Сливане в main:
git checkout main
git pull origin main
git merge add-title
git push origin main

Сливане чрез Pull Request в GitHub (алтернативно)

Отиди в GitHub:
След като качиш клона add-title, GitHub ще ти предложи да създадеш Pull Request (PR).

Създай Pull Request:
Избери main като base branch.
Избери add-title като compare branch.
Натисни Create Pull Request.

Слей Pull Request-а:
След преглед на кода от екипа (или самостоятелно), натисни Merge Pull Request.
Изтрий клона (по желание):

GitHub ще ти предложи да изтрие клона add-title след сливането.